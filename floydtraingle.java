import java.util.*;
class floydtraingle
{
public static void main(String ak[])
{
int n,num=1,c,d;
Scanner s=new Scanner(System.in);
System.out.println("Enter the number of rows of floyd's triangle you want");
n=s.nextInt();
for(c=1;c<=n;c++)
{
for(d=1;d<=c;d++)
{
System.out.println(num+"");
num++;
}
System.out.println();
}
}
}